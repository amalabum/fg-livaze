		<h3 style="font-weight:bold;">Notre Histoire </h3>
		<p>
			

Livraze a été fondée au mois d’avril 2022, à l'initiative de Fidel BASHIGE, le PDG de Fidba Graphics. Cette idée de bibliothèque remonte à plusieurs années mais elle est devenue une réalité en décembre 2022 lorsque ses collections ont atteint un niveau record, où 1000 abonnés pouvaient  emprunter des livres sans que les étagères restent vides. 

Le grand défi au départ  était que nous n'avions pas de livres. Seulement quelques dizaines et qui ne devaient pas répondre au grand besoin que nous ressentions : servir la ville de Goma en général. Aujourd'hui, la bibliothèque Livraze vise l'excellence dans la promotion de la lecture auprès d'une catégorie d'abonnés âgés de 13 ans et plus. 

Nous, Livraze, sommes passionnés par les livres depuis longtemps. Cette envie de lire ne devait pas rester uniquement la nôtre, alors un jour de l'année 2021, nous avons pensé à créer une bibliothèque physique, moderne et innovante. 

En avril 2022, nous avions mis en place une stratégie qui consistait à acheter des livres d'occasion en échange d'une somme d'argent selon les négociations avec les particuliers qui les échangeaient avec nous. Au bout de quelques jours, nous avions récolté une cinquantaine de livres, et le rêve commençait à prendre de l'ampleur. 

En décembre 2022, l'histoire retiendra que cette année-là, nous avons acheté des livres, en mettant de côté la peur d'échouer, à des revendeurs qui avaient en leur possession des ouvrages aux sujets très intéressants. Nous sommes passés de 400, à 700, à 1235 et le nombre de livres n'a cessé d'augmenter plus vite que les battements de cœur d'une musaraigne pygmée connue pour avoir le cœur le plus rapide du règne animal : aussi étonnant que cela puisse paraître, le cœur de ce petit rongeur bat à la vitesse impressionnante de 1000 battements par minute, soit 16 battements par seconde. C'est exceptionnel pour un si petit animal. 

Notre histoire peut devenir la vôtre !

