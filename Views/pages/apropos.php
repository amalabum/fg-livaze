Covid-19 a causé, en quelques mois, plus de 6,68 millions de décès dans le monde par contamination par contact direct avec les sécrétions du nez ou de la bouche d'une personne infectée, en l'embrassant par exemple ; par les aérosols dans l'air. Livraze veut s'inspirer des personnages d'une épidémie afin d'embrasser la population de la ville de Goma, en lui transmettant l'envie de lire beaucoup. 



