<!-- <h2> Concours pour étudiants  Livraze</h2> -->

<p>

Le concours livraze est un concours qui vise à récompenser 4 lauréats ayant lu et pouvant commenter en public une œuvre d'un auteur local ou national proposé par les organisateurs.

L'objectif de ce concours est de stimuler le goût de la lecture dans les universités  et de vendre des œuvres locales aux jeunes.
</p>
<p>
	<h3 style="font-weight:bold;">Les lauréats du concours seront récompensés par 4 prix :</h3>
    <ul style="margin-left:30px;margin-top:10px;">
		<li>Première place : 600 000 fc</li>

<li>Deuxième place : 400 000 fc</li>

<li>Troisième place : 200 000 fc</li>

<li>Quatrième prix : 2 mois d'abonnement à la bibliothèque livraze.</li>
	</ul>
<i style="margin:1em">	Il n'y a pas de frais de participation.  </i>

<h3 style="font-weight:bold;">	Conditions de participation au concours </h3>
<ul style="margin-left:30px;margin-top:10px;">
		<li>
 Avoir entre 16 et 30 ans </li>

<li> Etre de nationalité congolaise </li>

<li> Résider à Goma </li>

<li> Présenter une carte d'étudiant de l'une des institutions ou universités de Goma. </li>

<li> Soumettre la candidature dans le délai fixé par l'organisation.</li>  

<li> Avoir un compte Instagram et un profil Facebook </li>

<li> Être actif sur WhatsApp pendant toute la durée du concours.</li>
	</ul>
</p>
