Les 100 premières participations seront sélectionnées pour passer aux phases des éliminations 

</li>

<li>Les 4 canaux de communication utilisés par Livraze sont les suivants : 
	<ul style="margin:1em;">
		<li>L'adresse e-mail concours@livraze.com (pour accuser réception de la candidature de l'étudiant et encourager les participants qui n'ont pas  atteint la finale). 
