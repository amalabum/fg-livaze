<!-- Apropos  



Livraze est une bibliothèque privée de l'agence de communication globale Fidba Graphics. Avec des collections variées dans tous les domaines qui intéressent ses abonnés, la bibliothèque Livraze a marié la compétence des nouvelles technologies dans ses techniques d'approche afin d'encourager les jeunes à lire. 



Une bibliothèque jeune, moderne et riche avec des collections intéressantes qui font rêver ses abonnés.La bibliothèque Livraze, étant la toute première bibliothèque privée et moderne de la ville touristique de Goma, a mis en avant les besoins de ses abonnés en leur laissant le choix de consulter les documents ou les livres sur place ou de les emprunter. 



UN LIVRE NE TOMBE JAMAIS EN PANNE, UN SMARTPHONE OUI.

À l'ère du numérique, où la plupart des jeunes sont perdus dans le flux d'informations et d'intoxication, de vidéos moins productives sur TikTok et Instagram, de vidéos pornographiques facilement accessibles sur internet, l'envoi de sextapes sur des plateformes de messagerie comme WhatsApp, alors Livraze, la bibliothèque la plus proche de ses abonnés, se concentre sur l'initiation des jeunes à la lecture, une lecture sûre, sans piles à recharger ou à casser, sans tabou, une lecture informative, une lecture sur papier. Et ce, afin que les jeunes restent concentrés sur l'essentiel, pour leur bien et celui de nos communautés.







Mission

Covid-19 a causé, en quelques mois, plus de 6,68 millions de décès dans le monde par contamination par contact direct avec les sécrétions du nez ou de la bouche d'une personne infectée, en l'embrassant par exemple ; par les aérosols dans l'air. Livraze veut s'inspirer des personnages d'une épidémie afin d'embrasser la population de la ville de Goma, en lui transmettant l'envie de lire beaucoup. 



L'inspiration pour cette technique est venue du livre de Malcolm Gladwell : Le point de bascule. Pour comprendre l'émergence des modes, la naissance des best-sellers, ou tout autre changement mystérieux, il suffit de les considérer comme des épidémies. Ce livre est disponible dans la bibliothèque Livraze.



La mission de Livraze est d'encourager les jeunes de la ville de Goma, les touristes ou les visiteurs qui y séjournent à découvrir les livres de manière organisée. 



Les collections de livres de la bibliothèque Livraze sont multilingues : 

Des livres en anglais,  français, lingala et kiswahili. Des auteurs de plusieurs coins du globe ; des auteurs français, anglais, belges, camerounais, congolais de Kinshasa, Lubumbashi, Bukavu et Goma.



Notre Histoire 

Livraze a été fondée au mois d’avril 2022, à l'initiative de Fidel BASHIGE, le PDG de Fidba Graphics. Cette idée de bibliothèque remonte à plusieurs années mais elle est devenue une réalité en décembre 2022 lorsque ses collections ont atteint un niveau record, où 1000 abonnés pouvaient  emprunter des livres sans que les étagères restent vides. 

Le grand défi au départ  était que nous n'avions pas de livres. Seulement quelques dizaines et qui ne devaient pas répondre au grand besoin que nous ressentions : servir la ville de Goma en général. Aujourd'hui, la bibliothèque Livraze vise l'excellence dans la promotion de la lecture auprès d'une catégorie d'abonnés âgés de 13 ans et plus. 

Nous, Livraze, sommes passionnés par les livres depuis longtemps. Cette envie de lire ne devait pas rester uniquement la nôtre, alors un jour de l'année 2021, nous avons pensé à créer une bibliothèque physique, moderne et innovante. 

En avril 2022, nous avions mis en place une stratégie qui consistait à acheter des livres d'occasion en échange d'une somme d'argent selon les négociations avec les particuliers qui les échangeaient avec nous. Au bout de quelques jours, nous avions récolté une cinquantaine de livres, et le rêve commençait à prendre de l'ampleur. 

En décembre 2022, l'histoire retiendra que cette année-là, nous avons acheté des livres, en mettant de côté la peur d'échouer, à des revendeurs qui avaient en leur possession des ouvrages aux sujets très intéressants. Nous sommes passés de 400, à 700, à 1235 et le nombre de livres n'a cessé d'augmenter plus vite que les battements de cœur d'une musaraigne pygmée connue pour avoir le cœur le plus rapide du règne animal : aussi étonnant que cela puisse paraître, le cœur de ce petit rongeur bat à la vitesse impressionnante de 1000 battements par minute, soit 16 battements par seconde. C'est exceptionnel pour un si petit animal. 

Notre histoire peut devenir la vôtre !

Nous avons franchi le pas, pour ouvrir pour vous la bibliothèque la plus intelligente de la ville de Goma. Alors c'est à vous, bien sûr vous, d'apporter votre contribution pour que nous atteignions 1000 abonnés. Choisissez l'offre qui vous convient le mieux.  Abonnez-vous ici 

Soirées littéraires 



Présentation 

Afin de promouvoir la littérature locale et nationale, Livraze s'est donné pour mission d'organiser des cercles littéraires autour des livres des auteurs congolais de la diaspora ou vivant au pays. 

Les soirées de Livraze sont festives et elles réunissent des auteurs nationaux devant un public d'étudiants, d'entrepreneurs, d'avocats, de médecins, d'enseignants, de commerçants... pour échanger et se rencontrer autour de l'actualité du livre mais de manière vivante en compagnie d'un auteur local ou national.

Les cercles littéraires de Livraze font rêver et réunissent des auteurs nationaux devant un public d'étudiants, d'entrepreneurs, d'avocats, de médecins, d'enseignants, de commerçants... afin d'échanger, de se rencontrer autour de l'actualité du livre mais de manière vivante.



3 heures, un livre, un auteur, une culture à découvrir 

Les soirées littéraires de la bibliothèque  Livraze sont organisées de manière à valoriser les auteurs congolais, à promouvoir leurs œuvres afin d'encourager la génération montante à embrasser le monde des lettres.

Nos cercles littéraires sont décorés et aménagés avec le meilleur soin artistique afin de permettre aux participants de découvrir non seulement les livres mais aussi tout un éventail de cultures congolaises. 

La gastronomie congolaise : Nous invitons des chefs experts en gastronomie congolaise qui nous feront déguster des menus congolais.

Musique congolaise/Slam : Nous sélectionnons des musiciens, pianistes, guitaristes lors de nos soirées littéraires pour nous replonger au centre de la culture de notre grand pays. 

Peinture : Nous exposons des tableaux aux créativités exceptionnelles réalisés par des peintres locaux ou nationaux afin de valoriser la culture congolaise à travers la peinture.  

Photographie : Nous retraçons les souvenirs de nos soirées grâce au support photographique de nos partenaires photographes afin de garder en mémoire chaque moment de nos soirées.

Cinéma : Nous invitons des cinéastes locaux et nationaux à nous parler du cinéma congolais.



Programmes soirées littéraires  

Nos soirées littéraires ont lieu une fois par mois de 15h45 à 18h55.

15h45 - 16h45 : Accueil et cocktail (Gastronomie Congolaise) 

16h45 - 17h45 : Un livre, un auteur, Rencontre avec un auteur : discussions et échanges autour de son œuvre.

 17h45 - 17h55 : Musique live en lien avec l'univers de l'auteur. 

17h55 - 18h25 : Expositions (photographes, dessinateurs, peintres et cinéastes)

18h25 - 18h35 : Témoignages de librairies et bibliothèques universitaires ou indépendantes 

18h35 - 18h55 : Networking  et signature des livres de l'auteur autour d'un verre.

18h55 : Clôture de l'activité 









