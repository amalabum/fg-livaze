Concours pour étudiants  Livraze



Le concours livraze est un concours qui vise à récompenser 4 lauréats ayant lu et pouvant commenter en public une œuvre d'un auteur local ou national proposé par les organisateurs.

L'objectif de ce concours est de stimuler le goût de la lecture dans les universités  et de vendre des œuvres locales aux jeunes.



Les lauréats du concours seront récompensés par 4 prix :

Première place : 600 000 fc

Deuxième place : 400 000 fc

Troisième place : 200 000 fc

Quatrième prix : 2 mois d'abonnement à la bibliothèque livraze.

Il n'y a pas de frais de participation.  



Pour participer au concours, remplissez le formulaire ici. 

Conditions de participation 

- Avoir entre 16 et 30 ans 

- Etre de nationalité congolaise 

- Résider à Goma 

- Présenter une carte d'étudiant de l'une des institutions ou universités de Goma. 

- Soumettre la candidature dans le délai fixé par l'organisation.  

- Avoir un compte Instagram et un profil Facebook

- Être actif sur WhatsApp pendant toute la durée du concours.





Directives pour les participants au concours 

- Livraze ne demande aucune commission ou argent aux participants pendant toute la durée du concours, des éliminations à la grande finale.

Les 100 premières participations seront sélectionnées pour passer aux phases des éliminations


Les 4 canaux de communication utilisés par Livraze sont les suivants :

L'adresse e-mail concours@livraze.com (pour accuser réception de la candidature de l'étudiant et encourager les participants qui n'ont pas  atteint la finale). 

Le groupe WhatsApp géré par l'administration de Livraze dans lequel sont réunis tous les participants du concours. 

Les réseaux sociaux Instagram, Facebook, Twitter et Linkedin Livraze.

Le site officiel www.livraze.com 

NB : seul le numéro mentionné ici a le pouvoir de contacter les participants sur appel normal ou WhatsApp au sujet du concours.





Étapes du processus du concours 

Les éliminations sont divisées en 4 grandes étapes après réception des candidatures des élèves. 









Étape 0 : L'enregistrement des candidatures sera ouvert pendant une période de 7 jours. 



Étape 1 : Après avoir reçu toutes les candidatures pendant une semaine, les organisateurs publient la liste complète des 100 candidats sur les réseaux sociaux de Livraze et dans le groupe WhatsApp dans lequel se trouvent tous les participants.

Le 8e jour du concours, le compte à rebours est lancé sur les réseaux sociaux Facebook et Instagram de Livraze enjoignant les participants au concours à inviter leurs amis et leur famille à aimer et partager leur post publié sur la page Facebook officielle de Livraze ou à aimer leur post sur le compte Instagram officiel de Livraze. 100 likes sur Facebook représentent 1 % et 100 partages représentent 1 %. 100 likes sur Instagram représentent 1,5%. Le vote sur les réseaux sociaux dure au maximum 48 heures.



PHASE D'ÉLIMINATION 1 :



Le 11e jour du concours, les résultats des réseaux sociaux seront publiés le matin sur toutes les plateformes numériques de Livraze. 

Remarque : Les résultats du vote sur les réseaux sociaux représentent 15 % des points du concours. Seuls 50 étudiants ayant obtenu plus de % passeront à l'étape 2 du concours. La liste de leurs noms sera partagée sur les plateformes de communication de Livraze et celles de ses partenaires.




Si le participant ne dispose pas du livre choisi par le jury, il sera invité à contacter la bibliothèque Livraze en souscrivant à l'abonnement étudiant à 4,99 dollars par mois en cliquant sur www.livraze/etudiant.com. 



NB : Si l'étudiant a déjà un abonnement actif à la bibliothèque, il ne pourra pas payer à nouveau l’abonnement.



ÉLIMINATION PHASE 2 :

etape 3 : Travail en équipe : le 17ème jour du concours, les 50 candidats seront regroupés en 5 équipes de 10 étudiants chacune pour présenter le sujet du livre. Chaque équipe représentera un team leader  pour défendre son groupe devant le jury Alpha du concours. 



Le jury procédera ensuite aux questions-réponses  en partageant avec chaque groupe un chapitre du livre dont sera tiré le questionnaire. Tous les membres du groupe sont autorisés à participer à la séance de questions-réponses qui aura lieu dans les salons VIP 2  de l'agence de communication FIDBA GRAPHICS devant quelques invités et partenaires. NB : La présentation représente 40% des points du concours. Ces points sont répartis équitablement entre les membres de l'équipe.

A ce niveau, l'étudiant qui n'aura pas lu le travail ou qui sera absent sans raison valable lors de la séance de questions-réponses sera automatiquement éliminé. 

A l'issue de cette session, la liste des candidats sélectionnés pour la deuxième phase d'élimination sera publiée sur les plateformes de communication de Livraze et de ses partenaires.



Les points récoltés lors de la première phase d'élimination seront ajoutés à ceux de la deuxième phase d'élimination pour déterminer le % d’étudiants qui seront sectionnés. Seuls 25 élèves passeront à l'étape suivante du concours et la liste sera publiée après 24 heures de l'étape 3 du concours.



PHASE D'ELIMINATION 3 : 

SEMI-FINALE Soutien du public des candidats :



Le 19e jour du concours, les 25 candidats recevront chacun un crédit de 20 billets de 5 dollars chacun afin d'encourager leurs amis et leur famille à venir assister à leur prestation en demi-finale. Chaque ticket vendu représente 0,5% des points.



Étape 4 : Concours de rédaction : 

Le 21ème jour du concours, les 25 participants présélectionnés seront invités avec leur public à passer un test d'orthographe dans la salle de formation A et B de l'agence de communication Fidba Graphics. Les 25 participants seront répartis en 5 groupes de 5, trois personnes de chaque groupe seront éliminées en direct après la correction du jury Alpha. NB : chaque erreur représente 1,8% en moins. Chaque rature représente 0,6% en moins.

Les 10 Candidats présélectionnés lors de la troisième phase d'éliminations seront publiés en temps réel sur les réseaux sociaux de Livraze.  



Petite FINALE

Etape 5 : Le 22e jour du concours, les Candidats présélectionnés recevront chacun 20 crédits de billets d'entrée de 10 $ chacun afin d'inciter leur public à assister à leur spectacle de grande finale. Chaque billet vendu représente 0,5 % des points. En fonction des ventes de billets, 4 candidats seront éliminés 4 jours après cette étape. 



GRANDE FINALE 

Etape 5 : Le 28ème jour du concours, les 6 candidats s'affronteront devant un jury, 4 d'entre eux recevront les prix des 4 meilleurs étudiants du concours Livraze Student. 

La finale aura lieu dans un hôtel local.



